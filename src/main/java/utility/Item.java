package utility;

/**
 *
 * @author pierpaolo
 */
public class Item {
    
    private String body;
    
    private String title;

    public Item() {
    }

    public Item(String body, String title) {
        this.body = body;
        this.title = title;
    }

    public String getBody() {
        return body;
    }

    public void setBody(String body) {
        this.body = body;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    @Override
    public String toString() {
        return "Item{" + "body=" + body + ", title=" + title + '}';
    }
    
}
